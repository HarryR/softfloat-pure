use super::{
    expF64UI, float64_t, fracF64UI, packToF64, softfloat_propagateNaNF64, softfloat_roundPackToF64,
    softfloat_shiftRightJam64,
};

#[must_use]
pub const fn softfloat_addMagsF64(
    uiA: u64,
    uiB: u64,
    signZ: bool,
    roundingMode: u8,
    detectTininess: u8,
) -> (float64_t, u8) {
    // ------------------------------------------------------------------------
    let expA = expF64UI(uiA);
    let mut sigA = fracF64UI(uiA);
    let expB = expF64UI(uiB);
    let mut sigB = fracF64UI(uiB);
    // ------------------------------------------------------------------------
    let expDiff = expA.wrapping_sub(expB);
    let mut expZ;
    let mut sigZ;
    if 0 == expDiff {
        // --------------------------------------------------------------------
        if 0 == expA {
            return (
                float64_t {
                    v: uiA.wrapping_add(sigB),
                },
                0,
            );
        }
        if expA == 0x7FF {
            if 0 != (sigA | sigB) {
                return softfloat_propagateNaNF64(uiA, uiB);
            }
            return (float64_t { v: uiA }, 0);
        }
        expZ = expA;
        sigZ = (0x0020_0000_0000_0000 as u64)
            .wrapping_add(sigA)
            .wrapping_add(sigB);
        sigZ <<= 9;
    } else {
        // --------------------------------------------------------------------
        sigA <<= 9;
        sigB <<= 9;
        if expDiff < 0 {
            if expB == 0x7FF {
                if sigB != 0 {
                    return softfloat_propagateNaNF64(uiA, uiB);
                }
                return (packToF64(signZ, 0x7FF, 0), 0);
            }
            expZ = expB;
            if expA != 0 {
                sigA = sigA.wrapping_add(0x2000_0000_0000_0000);
            } else {
                sigA <<= 1;
            }
            sigA = softfloat_shiftRightJam64(sigA, expDiff.wrapping_neg() as u32);
        } else {
            if expA == 0x7FF {
                if sigA != 0 {
                    return softfloat_propagateNaNF64(uiA, uiB);
                }
                return (float64_t { v: uiA }, 0);
            }
            expZ = expA;
            if expB != 0 {
                sigB = sigB.wrapping_add(0x2000_0000_0000_0000);
            } else {
                sigB <<= 1;
            }
            sigB = softfloat_shiftRightJam64(sigB, expDiff as u32);
        }
        sigZ = (0x2000_0000_0000_0000 as u64)
            .wrapping_add(sigA)
            .wrapping_add(sigB);
        if sigZ < 0x4000_0000_0000_0000 {
            expZ = expZ.wrapping_sub(1);
            sigZ <<= 1;
        }
    }
    return softfloat_roundPackToF64(signZ, expZ, sigZ, roundingMode, detectTininess);
}

